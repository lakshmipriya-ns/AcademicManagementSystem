    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:/academicmanagementsystem", "root", "");
            if(rollno.getText().isEmpty()){
               JOptionPane.showMessageDialog(null, "Enter Register Number"); 
            }
            else{
            String rno = rollno.getText();
            String sql = "select rollno from uploadmarks where rollno='" + rno + "'";
            pst = con.prepareCall(sql);
            Statement st = con.createStatement();
            String name = "select stname from studentprofile where uname='" + rno + "'";
            rs = st.executeQuery(name);
            if (rs.next()) {
                String nme = rs.getString("stname");
                String mark = "select `s1_name`, `s2_name`, `s3_name`, `s4_name`, `s5_name`,`sub1`, `sub2`, `sub3`, `sub4`, `sub5` from uploadmarks where rollno='" + rno + "'";  
                rs = st.executeQuery(mark);
                if (rs.next()) {
                    dispose();
                    Marks mk = new Marks();
                    mk.lrno.setText(rno.toUpperCase());
                    mk.lname.setText(nme);
                    mk.s1.setText(rs.getString("s1_name"));
                    mk.s2.setText(rs.getString("s2_name"));
                    mk.s3.setText(rs.getString("s3_name"));
                    mk.s4.setText(rs.getString("s4_name"));
                    mk.s5.setText(rs.getString("s5_name"));
                    mk.m1.setText(rs.getString("sub1"));
                    mk.m2.setText(rs.getString("sub2"));
                    mk.m3.setText(rs.getString("sub3"));
                    mk.m4.setText(rs.getString("sub4"));
                    mk.m5.setText(rs.getString("sub5"));
                    if (Integer.parseInt(rs.getString("sub1")) >= 40) {
                        mk.p1.setText("P");
                    } else {
                        mk.p1.setText("RA");
                    }
                    if (Integer.parseInt(rs.getString("sub2")) >= 40) {
                        mk.p2.setText("P");
                    } else {
                        mk.p2.setText("RA");
                    }
                    if (Integer.parseInt(rs.getString("sub3")) >= 40) {
                        mk.p3.setText("P");
                    } else {
                        mk.p3.setText("RA");
                    }
                    if (Integer.parseInt(rs.getString("sub4")) >= 40) {
                        mk.p4.setText("P");
                    } else {
                        mk.p4.setText("RA");
                    }
                    if (Integer.parseInt(rs.getString("sub5")) >= 40) {
                        mk.p5.setText("P");
                    } else {
                        mk.p5.setText("RA");
                    }
                    mk.setVisible(true);
                }
            } else {
                JOptionPane.showMessageDialog(null, "Please Check Your Reg.No");
            }
            }
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(Result.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(Result.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

