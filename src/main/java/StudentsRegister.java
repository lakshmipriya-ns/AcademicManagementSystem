    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        try {
            Class.forName("com.mysql.cj.jdbc.Driver");
            con = DriverManager.getConnection("jdbc:mysql://localhost:/academicmanagementsystem", "root", "");
            String name = "select uname from studentdetails where uname=?";
            pst = con.prepareStatement(name);
            pst.setString(1, uname.getText());
            rs = pst.executeQuery();
           if(uname.getText().isEmpty() && passwrd.getText().isEmpty()){
               JOptionPane.showMessageDialog(null, "Please fill the data");
           }
           else{
              if (!rs.next()) {
                String sql = "INSERT INTO `studentdetails`(`uname`, `passwrd`)VALUES (?,?)";
                pst = con.prepareStatement(sql);
                pst.setString(1, uname.getText());
                pst.setString(2, passwrd.getText());
                pst.executeUpdate();
                JOptionPane.showMessageDialog(null, "Registered Successfully");
                uname.setText("");
                passwrd.setText("");
                uname.requestFocus();

            } else {
                JOptionPane.showMessageDialog(null, "Username already exist");
                uname.setText("");
                passwrd.setText("");
                uname.requestFocus();
            }
           }
           
        } catch (ClassNotFoundException ex) {
            Logger.getLogger(StudentsRegister.class.getName()).log(Level.SEVERE, null, ex);
        } catch (SQLException ex) {
            Logger.getLogger(StudentsRegister.class.getName()).log(Level.SEVERE, null, ex);
        }
    }//GEN-LAST:event_jButton2ActionPerformed

